class ConteudoMsg :
    def __init__ (self) :
        self.titulo = None
        self.conteudo = None

    def setTitulo(self, titulo) :
        self.titulo = titulo

    def getTitulo(self) :
        return self.titulo
    
    def setConteudo(self, conteudo) :
        self.conteudo = conteudo
    
    def getConteudo(self) :
        return self.conteudo

    